        Delivery service

        The subject of our project is goods delivery service. Our aim is to create an application, which will contain published orders with detailed information about the delivery of any food or supplies.
        This ideas main benefit is that it is useful for being helpful not only for customer, who orders stuff, but also for courier who seeks employment.
        Thus, basic components of an order are: list of products; delivery address; delivery cost; contact details of the customer; time, when the order is needed; details of the ordered goods. Projects structure reminds of the well-known structure «Freelance».

        The draft will be presented as a web site with the orders list and searching tools.
        Required condition to use an application will be registration.
        For the convenience, users will be able to choose some searching filters.
        Such as date filter, location filter and age-limitation filter (for example for buying cigarettes or an alcohol). Besides, by creating an order customer must specify the relevant time for order, after which the customer could delete it or extend.

        The described application focuses on users who do not have an opportunity or time to buy needed products and for those, who is looking for a job. Another benefit is the ability for customers to pay not only in cash but by food or other goods as well. Moreover, our project has no analogues in Ukraine.

        As an app development could be picked out this main ideas:

        • Adding list of couriers, who are ready to work at the moment;

        • Creating such feature in profile as «karma», which is influenced by the other users marks and reflected in top-customer (good tips, politeness) and top-courier (speed, mobility) charts on site.
